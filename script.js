// Addition

function add (a, b) {
    let c = a + b;
    console.log(c);
}

add(5, 5); // 10

// Subtraction

function subtract (a, b) {
    let c = a - b;
    console.log(c);
}

subtract(5, 5); // 0

// Multiplication

function multiply (a, b) {
    let c = a * b;
    console.log(c);
}

multiply(5, 5); // 25

// Division

function divide (a, b) {
    let c = a / b;
    console.log(c);
}

divide(10, 2); // 5

